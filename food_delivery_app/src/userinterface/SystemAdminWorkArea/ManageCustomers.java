    private Customer parseCustomer() {
        Customer customer = new Customer();
        UserAccount ua = new UserAccount();
        
        if (customer.setName(nameText.getText()) == false) {
            JOptionPane.showMessageDialog(this,"Name is not in a valid format.");
            return null;
        }
        
        if (customer.setEmail(emailText.getText()) == false) {
            JOptionPane.showMessageDialog(this, "Email is not in a valid format.");
            return null;
        }
        
        if (customer.setPhoneNumber(phoneText.getText()) == false) {
            JOptionPane.showMessageDialog(this, "Phone number is not in a valid format");
            return null;
        }
        
        if (customer.setAddress(addressText.getText()) == false) {
            JOptionPane.showMessageDialog(this, "Address is not in a valid format.");
            return null;
        }
        
        if (customer.setAge(ageText.getText()) == false) {
            JOptionPane.showMessageDialog(this, "Age is not in a valid format.");
            return null;
        }
        
        if (ua.setUsername(usernameText.getText()) == false) {
            JOptionPane.showMessageDialog(this, "Username is not in a valid format.");
            return null;
        }
        
        if (ua.setPassword(passwordText.getText()) == false) {
            JOptionPane.showMessageDialog(this, "Password is not in a valid format.");
            return null;
        }
        
        ua.setCustomer(customer);
        ua.setRole(new CustomerRole());
        
        customer.setUserAccount(ua);
        
        return customer;
    }
    
    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = customerTable.getSelectedRow();
        
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to be updated.");
            return;
        }
        
        Customer newCust = parseCustomer();
        if (newCust == null)
            return;
        
        
        // update user account
        Customer oldCust = system.getCustomerDirectory().getCustomerList().get(selectedRow);
        ArrayList<UserAccount> uaList = system.getUserAccountDirectory().getUserAccountList();
        int uaIdx = uaList.indexOf(oldCust.getUserAccount());
        if (system.getUserAccountDirectory().updateUserAccount(uaIdx, newCust.getUserAccount()) == false) {
            JOptionPane.showMessageDialog(this, "Username already exists.");
            return;
        }
        // update customer 
        system.getCustomerDirectory().updateCustomer(selectedRow, newCust);
        JOptionPane.showMessageDialog(this, "Customer updated successfully.");
        populateTableWithCustomers();
        clearFields();
    }//GEN-LAST:event_updateButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        Customer newCust = parseCustomer();
        if (newCust == null)
            return;
        
        if (system.getUserAccountDirectory().addUserAccount(newCust.getUserAccount()) == false) {
            JOptionPane.showMessageDialog(this, "Username already exists.");
            return;
        }
        system.getCustomerDirectory().addCustomer(newCust);
        JOptionPane.showMessageDialog(this, "Customer added successfully.");
        populateTableWithCustomers();
        clearFields();
    }//GEN-LAST:event_addButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = customerTable.getSelectedRow();
        
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to be deleted.");
            return;
        }
        
        Customer cust = system.getCustomerDirectory().getCustomerList().get(selectedRow);        
        //remove user account
        system.getUserAccountDirectory().removeUserAccount(cust.getUserAccount());
        // remove customer 
        system.getCustomerDirectory().deleteCustomer(cust);
        JOptionPane.showMessageDialog(this, "Customer deleted successfully.");
        populateTableWithCustomers();
    }//GEN-LAST:event_deleteButtonActionPerformed

