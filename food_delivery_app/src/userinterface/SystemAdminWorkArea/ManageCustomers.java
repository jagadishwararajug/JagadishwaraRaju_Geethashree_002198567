    public void populateTableWithCustomers() {
        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        model.setRowCount(0);
        for (Customer c: system.getCustomerDirectory().getCustomerList()) {
            Object[] row = new Object[6];
            row[0] = c;
            row[1] = c.getEmail();
            row[2] = c.getPhoneNumber();
            row[3] = c.getAddress();
            row[4] = c.getUserAccount().getUsername();
            row[5] = c.getAge();
            model.addRow(row);
        }
    }

